    @Override
    public InterfaceDTO getData() {
        EntregaDTO pdto = new EntregaDTO();

        String id = this.JTA_id.getText();
        String chave = this.jTA_chave.getText();
        String enderid = this.jTA_enderid.getText();
        String valor = this.jTA_valor.getText();

        if (id != null && !id.isEmpty()) {
            pdto.id = Integer.valueOf(id);
        }
        pdto.chave = chave;
        if (enderid != null && !enderid.isEmpty()) {
            pdto.endereco = new Endereco(Integer.valueOf(enderid), null, null, null, null, null);
        }
        if (valor != null && !valor.isEmpty()) {
            pdto.valor = Double.valueOf(valor);
        }
               
        return (InterfaceDTO) pdto;
    }
    
    @Override
    public void setDados(InterfaceDTO dto) {
        EntregaDTO entregaDTO = (EntregaDTO) dto;
        this.JTA_id.setText(String.valueOf(entregaDTO.id));
        this.jTA_chave.setText(entregaDTO.chave);
        this.jTA_enderid.setText(String.valueOf(entregaDTO.endereco.getId()));
        this.jTA_valor.setText(String.valueOf(entregaDTO.valor));
    }

    @Override
    public void clear() {
        this.JTA_id.setText("");
        this.jTA_chave.setText("");
        this.jTA_enderid.setText("");
        this.jTA_valor.setText("");
    }

